#include "libft.h"

void	*ft_memalloc(size_t size)
{
	unsigned char	*ptr;
	size_t			i;

	i = 0;
	ptr = (unsigned char *)malloc(size);
	if (ptr)
	{
		while (i < size)
		{
			ptr[i] = 0;
			i++;
		}
	}
	return ((void *)ptr);
}
